// Function to update the week number in the HTML and populate the duty roster
function updateWeekAndDutyRoster() {
// Update the week number
const titleElement = document.getElementById('titleElement');
const weekNumberSpan = titleElement.querySelector('span');
weekNumberSpan.textContent = currentWeek + 1;

// Populate the duty roster for the current week
populateDutyRoster();
}

// Function to handle when the "Previous Week" button is clicked
function previousWeek() {
if (currentWeek > 0) { // Check if it's not the first week
 currentWeek--; // Decrement the week number
 updateWeekAndDutyRoster(); // Update the week number and populate the duty roster
}
}

// Function to handle when the "Next Week" button is clicked
function nextWeek() {
// Assuming 4 weeks in May, adjust the condition as needed for other months
if (currentWeek < 3) { // Assuming 4 weeks in May
 currentWeek++; // Increment the week number
 updateWeekAndDutyRoster(); // Update the week number and populate the duty roster
}
}

